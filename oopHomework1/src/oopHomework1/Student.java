package oopHomework1;

public class Student extends User {
	
	
	
	String email;
	String password;
	
	public  Student()
	{
		
	}
	
	

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}
	
}
